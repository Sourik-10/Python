# A virtual environment is an isolated Python environment that allows you
# to install and manage project-specific dependencies without affecting
# the system-wide Python installation.


# Install the "requests" library
# pip install requests


# Install a specific version of NumPy
# pip install numpy==1.20.0


# List all installed packages in the active virtual environment
# pip list


# Upgrade the "requests" library to the latest version
# pip install --upgrade requests


# Uninstall the "requests" library
# pip uninstall requests


# Create a requirements.txt file with all installed packages
# pip freeze > requirements.txt


# Install all packages listed in requirements.txt
# pip install -r requirements.txt


# Deactivate the currently active virtual environment
# deactivate

